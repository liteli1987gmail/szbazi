    print(f"插入书籍：{book_name}（{book_id}）")

    # 插入段落
    total_paragraphs = 0
    for chapter in SANMING_TONGHUI_JUAN1["chapters"]:
        chapter_title = chapter["title"]
        content = chapter["content"].strip()

        # 将内容按句子或段落分割成小段落
        paragraphs = content.split("\n\n")
        for idx, para in enumerate(paragraphs, start=1):
            para = para.strip()
            if not para:
                continue
            paragraph_id = f"{book_id}_{chapter_title}_{idx}"
            db.insert_paragraph(paragraph_id, book_id, idx, para)
            total_paragraphs += 1

        print(f"  章节「{chapter_title}」: {len(paragraphs)} 个段落")

    print(f"\n✅ 《{book_name}》已填充完成，共 {len(SANMING_TONGHUI_JUAN1['chapters'])} 章，{total_paragraphs} 个段落")
