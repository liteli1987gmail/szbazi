def _conn(db_path):
    conn = sqlite3.connect(db_path)
    conn.row_factory = sqlite3.Row
    return conn


def _ensure_schema(db_path):
    with _conn(db_path) as conn:
        conn.executescript("""
            CREATE TABLE IF NOT EXISTS books (
                book_id   TEXT PRIMARY KEY,
                book_name TEXT NOT NULL
            );
            CREATE TABLE IF NOT EXISTS paragraphs (
                paragraph_id TEXT PRIMARY KEY,
                book_id      TEXT NOT NULL,
                sequence     INTEGER NOT NULL,
                content      TEXT NOT NULL,
                FOREIGN KEY (book_id) REFERENCES books(book_id)
            );
        """)


def _insert_book(conn, book_id, book_name):
    conn.execute("INSERT OR IGNORE INTO books VALUES (?, ?)", (book_id, book_name))


def _insert_paragraph(conn, paragraph_id, book_id, sequence, content):
    conn.execute(
        "INSERT OR IGNORE INTO paragraphs VALUES (?, ?, ?, ?)",
        (paragraph_id, book_id, sequence, content),
    )


def seed_ditian_sui(db_path: str = "data/bazi_knowledge.db"):
    os.makedirs(os.path.dirname(db_path), exist_ok=True)
    _ensure_schema(db_path)

    book_id = DITIAN_SUI["book_id"]
    book_name = DITIAN_SUI["book_name"]

    # 删除旧书及其段落
    with _conn(db_path) as conn:
        conn.execute("DELETE FROM paragraphs WHERE book_id=?", (book_id,))
        conn.execute("DELETE FROM books WHERE book_id=?", (book_id,))

    # 插入书籍
    with _conn(db_path) as conn:
        _insert_book(conn, book_id, book_name)
    print(f"插入书籍：{book_name}（{book_id}）")

    # 插入段落
    total_paragraphs = 0
    for chapter in DITIAN_SUI["chapters"]:
        chapter_title = chapter["title"]
        content = chapter["content"].strip()

        # 将内容按段落分割
        paragraphs = content.split("\n\n")
        with _conn(db_path) as conn:
            for idx, para in enumerate(paragraphs, start=1):
                para = para.strip()
                if not para:
                    continue
                paragraph_id = f"{book_id}_{chapter_title}_{idx}"
                _insert_paragraph(conn, paragraph_id, book_id, idx, para)
                total_paragraphs += 1

        print(f"  章节「{chapter_title}」: {len(paragraphs)} 个段落")

    print(f"\n✅ 《{book_name}》已填充完成，共 {len(DITIAN_SUI['chapters'])} 章，{total_paragraphs} 个段落")
    print(f"   数据库：{db_path}")


if __name__ == "__main__":
    seed_ditian_sui()